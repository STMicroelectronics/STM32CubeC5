system_status_t mx_pwr_enter_low_power(void)
{
  /* Clear both standby and stop flags */
  HAL_PWR_CleanPreviousSystemPowerMode();

  /* Enter the MCU in stop mode */
  HAL_PWR_EnterStopMode(HAL_PWR_LOW_PWR_MODE_WFI, HAL_PWR_STOP1_MODE);

  return SYSTEM_OK;
}

system_status_t mx_pwr_exit_low_power(void)
{
  /* This API is the runtime API generated by RCC module */
  return (mx_rcc_init());
}
