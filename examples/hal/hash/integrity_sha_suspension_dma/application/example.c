/** ########## Step 1 ##########
  * Initializes the HASH peripheral.
  */

app_status_t app_init(void)
{
  app_status_t return_status = EXEC_STATUS_ERROR;

  /* Initializes the HASH peripheral.for polling process */
  pHASH = mx_example_hash_init();
  if (pHASH == NULL)
  {
    goto _app_init_exit;
  }

  /* Register the user hash error Callback*/
  if (HAL_HASH_RegisterErrorCpltCallback(pHASH, ErrorCallback) != HAL_OK)
  {
    goto _app_init_exit;
  }
  /* Register the user hash digest Callback*/
  if (HAL_HASH_RegisterDigestComputationCpltCallback(pHASH, DigestCpltCallback) != HAL_OK)
  {
    goto _app_init_exit;
  }
  /* Register the user hash suspend Callback*/
  if (HAL_HASH_RegisterSuspendCpltCallback(pHASH, SuspendCallback) != HAL_OK)
  {
    goto _app_init_exit;
  }

  PRINTF("[INFO] Step 1: HASH initialization COMPLETED.\n");

  return_status = EXEC_STATUS_INIT_OK;

_app_init_exit:
  return return_status;
} /* end app_init */


/** ########## Step 2 ##########
  * Demonstrates how to suspend and to resume a computation process in DMA mode,
  * using SHA256 algorithm.
  * This step is scheduled as follows:
  * Step 2.1: Suspends DMA computation process.
  * Step 2.2: Computes and verifies the second message in polling mode.
  * Step 2.3: Resumes the computation process in DMA mode.
  */
app_status_t app_process(void)
{
  app_status_t return_status = EXEC_STATUS_ERROR;

  /** ########## Step 2.1 ##########
    * Suspends DMA computation process.
    */

  /* Compute the HASH message in DMA mode */
  if (HAL_HASH_Compute_DMA(pHASH,
                           MsgSHA256LongMsg, 6400,
                           SHA256_Output, MESSAGE_BUFFER_SIZE, &SHA256_OutputSize) != HAL_OK)
  {
    goto _app_process_exit;
  }

  /* Request to suspend computation*/
  if (HAL_HASH_RequestSuspendComputation(pHASH) != HAL_OK)
  {
    goto _app_process_exit;
  }
  /* Save HASH context */
  HAL_HASH_SaveContext(pHASH, &p_hash_context);

  PRINTF("[INFO] Step 2.1: Suspend computation process in DMA mode COMPLETED.\n");

  /** ########## Step 2.2 ##########
    * Computes and verifies the second message in polling mode.
    */

  /* Compute the message */
  if (HAL_HASH_Compute(pHASH, Message, 163, computed_hash_message, MESSAGE_BUFFER_SIZE, &ComputedSize, 1000) != HAL_OK)
  {
    goto _app_process_exit;
  }

  /* Verify the computed hash message */
  if (memcmp(computed_hash_message, ExpectedHash, 32) != 0)
  {
    goto _app_process_exit;
  }

  PRINTF("[INFO] Step 2.2: Computation and verification of the second message COMPLETED.\n");

  /** ########## Step 2.3 ##########
    * Resumes and verifies the computation process in DMA mode.
    */

  /* Restore the context */
  HAL_HASH_RestoreContext(pHASH, &p_hash_context);

  /* Resume the computation process */
  if (HAL_HASH_ResumeComputation(pHASH) != HAL_OK)
  {
    goto _app_process_exit;
  }
  /* Wait for one of these HASH interrupts: compute complete or compute error.*/
  while ((ComputeCpltCb == 0) && (ComputeErrorCb == 0))
  {
    /* Put the CPU in Wait For Interrupt state.*/
    __WFI();
  }
  if (ComputeErrorCb == 1)
  {
    goto _app_process_exit;
  }
  /* Verify the computed hash message */
  if (memcmp(SHA256_Output, ExpectedSHA256LongMsg, 32) != 0)
  {
    goto _app_process_exit;
  }
  PRINTF("[INFO] Step 2.3: Resumption of computation DMA process COMPLETED.\n");

  return_status = EXEC_STATUS_OK;

_app_process_exit:
  return return_status;
} /* end app_process*/


